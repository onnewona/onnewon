@loader.tds
class FancyFontsMod(loader.Module):
    """Create fancy font text with more than 10 styles available"""

    strings = {
		"name": "AutoWord", 
		"no_args": "🐝 <b>Please, write under command...</b>"
	}
	        
    async def ttelegacmd(self, message: Message) -> None:
        """- <text> automatic translate in a coding font"""
        args = utils.get_args_raw(message)
        if not args:
            await utils.answer(message, self.strings("no_args"))
        else:
            for i in FONT:
                await message.reply(f"{str.translate(args, str.maketrans(BASE, i))}")

      
